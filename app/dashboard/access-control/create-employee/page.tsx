"use client";       

import { useState, useEffect } from "react";
import { useRouter } from "next/navigation";
import { DashboardHeader } from "@/components/mainCOMP/dashboard-header";
import { EnhancedSidebar } from "@/components/mainCOMP/enhanced-sidebar";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Switch } from "@/components/ui/switch";
import { ScrollArea } from "@/components/ui/scroll-area";
import {
  UserPlus,
  Users,
  CheckCircle,
  XCircle,
  Loader2,
  ArrowRight,
} from "lucide-react";
import { PermissionsDropdown } from "@/components/access-control/PermissionsDropdown";
import axiosInstance from "@/lib/axiosInstance";

// Types
interface Permission {
  id: number;
  name: string;
  name_ar?: string;
  name_en?: string;
  guard_name: string;
  team_id: number | null;
  description: string | null;
  created_at: string;
  updated_at: string;
}

interface PermissionsResponse {
  status: string;
  data: Permission[];
  grouped: {
    [key: string]: Permission[];
  };
  templates: {
    [key: string]: {
      [key: string]: string;
    };
  };
}

interface CreateEmployeeRequest {
  first_name: string;
  last_name: string;
  email: string;
  phone: string;
  password: string;
  active: boolean;
  role_ids: number[];
  permissions: string[];
}

export default function CreateEmployeePage() {
  const router = useRouter();
  const [formData, setFormData] = useState<CreateEmployeeRequest>({
    first_name: "",
    last_name: "",
    email: "",
    phone: "",
    password: "",
    active: true,
    role_ids: [],
    permissions: [],
  });
  const [selectedPermissions, setSelectedPermissions] = useState<{
    [key: string]: boolean;
  }>({});
  const [permissions, setPermissions] = useState<PermissionsResponse | null>(
    null,
  );
  const [permissionsLoading, setPermissionsLoading] = useState(false);
  const [createLoading, setCreateLoading] = useState(false);
  const [createError, setCreateError] = useState<string | null>(null);
  const [createSuccess, setCreateSuccess] = useState(false);

  // Fetch permissions
  useEffect(() => {
    const fetchPermissions = async () => {
      setPermissionsLoading(true);
      try {
        const response =
          await axiosInstance.get<PermissionsResponse>("/v1/permissions");
        setPermissions(response.data);
      } catch (err: any) {
        console.error("Error fetching permissions:", err);
      } finally {
        setPermissionsLoading(false);
      }
    };

    fetchPermissions();
  }, []);

  // Handle permission change
  const handlePermissionChange = (permissionName: string, checked: boolean) => {
    setSelectedPermissions((prev) => ({
      ...prev,
      [permissionName]: checked,
    }));
  };

  // Handle group permission change
  const handleGroupPermissionChange = (
    groupName: string,
    checked: boolean,
  ) => {
    if (!permissions || !permissions.grouped[groupName]) return;

    const groupPermissions = permissions.grouped[groupName];
    const newSelectedPermissions = { ...selectedPermissions };

    groupPermissions.forEach((permission) => {
      newSelectedPermissions[permission.name] = checked;
    });

    setSelectedPermissions(newSelectedPermissions);
  };

  // Create employee
  const createEmployee = async () => { 
    setCreateLoading(true);
    setCreateError(null);

    try {
      // Convert selected permissions to array
      const selectedPermissionsArray = Object.entries(selectedPermissions)
        .filter(([_, selected]) => selected)
        .map(([permissionName, _]) => permissionName);

      const requestData = {
        ...formData,
        permissions: selectedPermissionsArray,
        role_ids: [],
      };

      await axiosInstance.post("/v1/employees", requestData);

      setCreateSuccess(true);

      // Reset form
      setFormData({
        first_name: "",
        last_name: "",
        email: "",
        phone: "",
        password: "",
        active: true,
        role_ids: [],
        permissions: [],
      });
      setSelectedPermissions({});

      // Redirect after 2 seconds
      setTimeout(() => {
        router.push("/dashboard/access-control");
      }, 2000);
    } catch (err: any) {
      console.error("Error creating employee:", err);
      setCreateError(
        err.response?.data?.message || "حدث خطأ في إنشاء الموظف",
      );
    } finally {
      setCreateLoading(false);
    }
  };

  return (
    <div className="flex min-h-screen flex-col h-screen bg-gray-50">
        <DashboardHeader />
      <div className="flex flex-1 flex-col md:flex-row overflow-hidden">
      <EnhancedSidebar />
        <div className="flex-1 overflow-y-auto p-4 sm:p-6 md:p-8">
          <div className="max-w-4xl mx-auto">
            {/* Header */}
            <div className="mb-6 sm:mb-8">
              <div className="flex items-center gap-3 sm:gap-4 mb-2">
                <Button
                  variant="ghost"
                  size="sm"
                  onClick={() => router.back()}
                  className="p-2"
                >
                  <ArrowRight className="h-4 w-4" />
                </Button>
                <div className="flex items-center gap-2 sm:gap-3">
                  <div className="p-1.5 sm:p-2 bg-black rounded-lg">
                    <UserPlus className="h-4 w-4 sm:h-6 sm:w-6 text-white" />
                  </div>
                  <h1 className="text-lg sm:text-xl md:text-2xl font-bold text-black">
                    إضافة موظف جديد
                  </h1>
                </div>
              </div>
              <p className="text-gray-600 text-sm sm:text-base text-right mt-2 pr-10 sm:pr-12">
                قم بإنشاء حساب جديد للموظف وتخصيص الصلاحيات المناسبة
              </p>
            </div>

            {/* Content Card */}
            <div className="bg-white rounded-lg shadow-sm border border-gray-200">
              <ScrollArea className="max-h-[calc(100vh-300px)]">
                <div className="p-4 sm:p-6 space-y-4 sm:space-y-6 md:space-y-8">
                  {/* Success Message */}
                  {createSuccess && (
                    <div className="bg-green-50 border border-green-200 rounded-lg p-4 flex items-center gap-3">
                      <CheckCircle className="h-5 w-5 text-green-600" />
                      <span className="text-green-800 font-medium">
                        تم إنشاء الموظف بنجاح! جاري إعادة التوجيه...
                      </span>
                    </div>
                  )}

                  {/* Error Message */}
                  {createError && (
                    <div className="bg-red-50 border border-red-200 rounded-lg p-4 flex items-center gap-3">
                      <XCircle className="h-5 w-5 text-red-600" />
                      <span className="text-red-800 font-medium">
                        {createError}
                      </span>
                    </div>
                  )}

                  {/* Basic Information Section */}
                  <div className="space-y-4 sm:space-y-6">
                    <div className="flex items-center gap-2 sm:gap-3 pb-2 border-b border-gray-200">
                      <div className="p-1.5 sm:p-2 bg-gray-100 rounded-lg">
                        <Users className="h-4 w-4 sm:h-5 sm:w-5 text-gray-700" />
                      </div>
                      <h3 className="text-lg sm:text-xl font-semibold text-black">
                        المعلومات الأساسية
                      </h3>
                    </div>

                    <div className="grid grid-cols-1 sm:grid-cols-2 gap-4 sm:gap-6">
                      <div className="space-y-2">
                        <Label
                          htmlFor="first_name"
                          className="text-sm font-medium text-gray-700"
                        >
                          الاسم الأول *
                        </Label>
                        <Input
                          id="first_name"
                          value={formData.first_name}
                          onChange={(e) =>
                            setFormData((prev) => ({
                              ...prev,
                              first_name: e.target.value,
                            }))
                          }
                          placeholder="أدخل الاسم الأول"
                          className="border-gray-300 focus:border-black focus:ring-black text-sm sm:text-base"
                        />
                      </div>

                      <div className="space-y-2">
                        <Label
                          htmlFor="last_name"
                          className="text-sm font-medium text-gray-700"
                        >
                          الاسم الأخير *
                        </Label>
                        <Input
                          id="last_name"
                          value={formData.last_name}
                          onChange={(e) =>
                            setFormData((prev) => ({
                              ...prev,
                              last_name: e.target.value,
                            }))
                          }
                          placeholder="أدخل الاسم الأخير"
                          className="border-gray-300 focus:border-black focus:ring-black text-sm sm:text-base"
                        />
                      </div>

                      <div className="space-y-2">
                        <Label
                          htmlFor="email"
                          className="text-sm font-medium text-gray-700"
                        >
                          البريد الإلكتروني *
                        </Label>
                        <Input
                          id="email"
                          type="email"
                          value={formData.email}
                          onChange={(e) =>
                            setFormData((prev) => ({
                              ...prev,
                              email: e.target.value,
                            }))
                          }
                          placeholder="example@company.com"
                          className="border-gray-300 focus:border-black focus:ring-black text-sm sm:text-base"
                        />
                      </div>

                      <div className="space-y-2">
                        <Label
                          htmlFor="phone"
                          className="text-sm font-medium text-gray-700"
                        >
                          رقم الهاتف *
                        </Label>
                        <Input
                          id="phone"
                          value={formData.phone}
                          onChange={(e) =>
                            setFormData((prev) => ({
                              ...prev,
                              phone: e.target.value,
                            }))
                          }
                          placeholder="+966501234567"
                          className="border-gray-300 focus:border-black focus:ring-black text-sm sm:text-base"
                        />
                      </div>

                      <div className="space-y-2">
                        <Label
                          htmlFor="password"
                          className="text-sm font-medium text-gray-700"
                        >
                          كلمة المرور *
                        </Label>
                        <Input
                          id="password"
                          type="password"
                          value={formData.password}
                          onChange={(e) =>
                            setFormData((prev) => ({
                              ...prev,
                              password: e.target.value,
                            }))
                          }
                          placeholder="كلمة مرور قوية"
                          className="border-gray-300 focus:border-black focus:ring-black text-sm sm:text-base"
                        />
                      </div>

                      <div className="space-y-2">
                        <Label
                          htmlFor="active"
                          className="text-sm font-medium text-gray-700"
                        >
                          حالة الحساب
                        </Label>
                        <div className="flex items-center gap-2 sm:gap-3 p-2 sm:p-3 border border-gray-300 rounded-lg">
                          <Switch
                            id="active"
                            checked={formData.active}
                            onCheckedChange={(checked) =>
                              setFormData((prev) => ({
                                ...prev,
                                active: checked,
                              }))
                            }
                            className="data-[state=checked]:bg-black"
                          />
                          <span className="text-xs sm:text-sm text-gray-600">
                            {formData.active ? "نشط" : "غير نشط"}
                          </span>
                        </div>
                      </div>

                      <div className="space-y-2 sm:col-span-2">
                        <Label
                          htmlFor="permissions"
                          className="text-sm font-medium text-gray-700"
                        >
                          الصلاحيات
                        </Label>
                        <PermissionsDropdown
                          permissions={permissions}
                          selectedPermissions={selectedPermissions}
                          handlePermissionChange={handlePermissionChange}
                          handleGroupPermissionChange={handleGroupPermissionChange}
                          isLoading={permissionsLoading}
                        />
                      </div>
                    </div>
                  </div>
                </div>
              </ScrollArea>

              {/* Footer Actions */}
              <div className="flex flex-col sm:flex-row justify-end gap-2 sm:gap-3 pt-3 sm:pt-4 border-t border-gray-200 px-4 sm:px-6 pb-3 sm:pb-4">
                <Button
                  variant="outline"
                  onClick={() => router.back()}
                  className="border-gray-300 text-gray-700 hover:bg-gray-50 text-sm sm:text-base w-full sm:w-auto"
                >
                  إلغاء
                </Button>
                <Button
                  onClick={createEmployee}
                  disabled={
                    createLoading ||
                    !formData.first_name ||
                    !formData.last_name ||
                    !formData.email ||
                    !formData.phone ||
                    !formData.password
                  }
                  className="bg-black hover:bg-gray-800 text-white disabled:bg-gray-400 text-sm sm:text-base w-full sm:w-auto"
                >
                  {createLoading ? (
                    <>
                      <Loader2 className="h-3 w-3 sm:h-4 sm:w-4 ml-1 sm:ml-2 animate-spin" />
                      <span className="text-xs sm:text-sm">جاري الإنشاء...</span>
                    </>
                  ) : (
                    <>
                      <UserPlus className="h-3 w-3 sm:h-4 sm:w-4 ml-1 sm:ml-2" />
                      <span className="text-xs sm:text-sm">إنشاء الموظف</span>
                    </>
                  )}
                </Button>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}

