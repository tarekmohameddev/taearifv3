/**
 * Adds demo properties to existing properties for demonstration purposes
 * @param {Array} existingProperties - Array of existing properties
 * @param {boolean} includeDemoProperties - Whether to include demo properties
 * @returns {Array} Combined array of properties
 */
export function addDemoProperties(existingProperties = [], includeDemoProperties = true) {
  if (!includeDemoProperties) {
    return existingProperties;
  }

  // Check if any existing properties have coordinates
  const hasPropertiesWithCoords = existingProperties.some(
    (prop) =>
      (prop.latitude && prop.longitude) ||
      (prop.location?.latitude && prop.location?.longitude)
  );

  // If there are no properties with coordinates, add demo properties
  // Or if there are very few properties, add some demo ones
  if (!hasPropertiesWithCoords || existingProperties.length < 5) {
    return [...demoProperties, ...existingProperties];
  }

  return existingProperties;
}

/**
 * Gets only demo properties
 * @returns {Array} Array of demo properties
 */
export function getDemoProperties() {
  return demoProperties;
}
